/* Linguagem */
let currentLang = localStorage.getItem('dn-lang') || 'pt';

function applyLang(lang) {
  currentLang = lang;
  localStorage.setItem('dn-lang', lang);
  document.documentElement.setAttribute('data-lang', lang);

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = i18n[lang][key];
    if (!val) return;

    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
      return;
    }

    if (key.includes('.btn') || key.includes('hero.btn') || key.includes('checker.btn')) {
      el.textContent = val;
      return;
    }

    el.innerHTML = val;
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    const val = i18n[lang][key];
    if (val) el.placeholder = val;
  });

  // Update lang toggle button
  const lb = document.querySelector('.lang-toggle');
  if (lb) lb.textContent = i18n[lang]['lang.btn'];
}

