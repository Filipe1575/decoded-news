function simularVerificacao() {
  const input  = document.getElementById('news-input');
  const result = document.getElementById('checker-result');
  if (!input || !result) return;

  const txt = input.value.trim().toLowerCase();
  if (!txt) { alert(currentLang === 'pt' ? 'Digite um texto para analisar!' : 'Enter text to analyze!'); return; }

  const fake = ['grátis','gratis','clique agora','compartilhe urgente','exclusivo','chocante',
    'inacreditável','governo esconde','médicos odeiam','ganhe','brinde','milagre','cura garantida',
    'aprovado agora','confirmado','vazou','proibido','segredo','não querem que você saiba',
    'free','share now','shocking','doctors hate','miracle','government hiding'];
  const real = ['segundo pesquisa','de acordo com','estudo publicado','fonte oficial','ministério',
    'universidade','cientistas','agência','publicado em','according to','study published','official source'];

  let pF = 0, pR = 0;
  fake.forEach(p => { if (txt.includes(p)) pF++; });
  real.forEach(p => { if (txt.includes(p)) pR++; });

  let html, border;
  const pt = currentLang === 'pt';

  if (pF >= 2) {
    html = `<strong>${pt?'Alta chance de Fake News!':'High chance of Fake News!'}</strong><br>${pt?`Detectadas ${pF} expressão(ões) suspeita(s). Verifique a fonte.`:`Found ${pF} suspicious expression(s). Verify the source.`}`;
    border = '#dc2626';
  } else if (pF === 1 && pR === 0) {
    html = `<strong>${pt?'Atenção!':'Attention!'}</strong><br>${pt?'Contém um sinal de alerta. Confirme em fontes confiáveis.':'Contains a warning sign. Confirm with reliable sources.'}`;
    border = '#d97706';
  } else if (pR >= 1) {
    html = `<strong>${pt?'Aparenta ser confiável.':'Appears to be reliable.'}</strong><br>${pt?'Contém referências a fontes. Confirme antes de compartilhar.':'Contains source references. Confirm before sharing.'}`;
    border = '#16a34a';
  } else {
    html = `<strong>${pt?'Inconclusivo.':'Inconclusive.'}</strong><br>${pt?'Pesquise em Agência Lupa ou Aos Fatos.':'Search on fact-checking agencies to confirm.'}`;
    border = '#64748b';
  }

  result.innerHTML = html;
  result.style.borderLeftColor = border;
  result.classList.remove('hidden');
}

